/// Convert a Windows scan code + flags to a USB HID usage code.
/// @param scanCode  Windows MakeCode from RAWKEYBOARD
/// @param flags     CursorShare keyboard flags (bit 0 = E0 prefix)
/// @return HID usage code, or 0 if unmapped
inline uint8_t ScanCodeToHid(uint16_t scanCode, uint8_t flags) {
  uint8_t sc = static_cast<uint8_t>(scanCode & 0xFF);
  if (flags & 0x01) { // E0 prefix
    return kScanToHidE0[sc];
  }
  return kScanToHid[sc];
}

/// Check if a HID usage code is a modifier key (0xE0-0xE7).
inline bool IsHidModifier(uint8_t hidCode) {
  return hidCode >= 0xE0 && hidCode <= 0xE7;
}

/// Get the modifier bit for a HID modifier usage code.
/// HID spec: E0=Left Ctrl(bit0), E1=Left Shift(bit1), E2=Left Alt(bit2),
///           E3=Left GUI(bit3), E4=Right Ctrl(bit4), E5=Right Shift(bit5),
///           E6=Right Alt(bit6), E7=Right GUI(bit7)
inline uint8_t HidModifierBit(uint8_t hidCode) {
  if (hidCode >= 0xE0 && hidCode <= 0xE7) {
    return 1 << (hidCode - 0xE0);
  }
  return 0;
}

